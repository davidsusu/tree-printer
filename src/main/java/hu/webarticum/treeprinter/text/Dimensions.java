package hu.webarticum.treeprinter.text;

public class Dimensions {
    
    private final int width;
    
    private final int height;
    

    public Dimensions(int width, int height) {
        this.width = width;
        this.height = height;
    }
    

    public int width() {
        return width;
    }
    
    public int height() {
        return height;
    }
    
}
